package com.adm.model;

import java.sql.Date;

public class AdmVO {
	private String adm_no;
	private String adm_id;
	private String adm_psw;
	private Date adm_createdate;
	private String adm_status;
	public String getAdm_no() {
		return adm_no;
	}
	public void setAdm_no(String adm_no) {
		this.adm_no = adm_no;
	}
	public String getAdm_id() {
		return adm_id;
	}
	public void setAdm_id(String adm_id) {
		this.adm_id = adm_id;
	}
	public String getAdm_psw() {
		return adm_psw;
	}
	public void setAdm_psw(String adm_psw) {
		this.adm_psw = adm_psw;
	}
	public Date getAdm_createdate() {
		return adm_createdate;
	}
	public void setAdm_createdate(Date adm_createdate) {
		this.adm_createdate = adm_createdate;
	}
	public String getAdm_status() {
		return adm_status;
	}
	public void setAdm_status(String adm_status) {
		this.adm_status = adm_status;
	}
	
	
	
	
	
}
